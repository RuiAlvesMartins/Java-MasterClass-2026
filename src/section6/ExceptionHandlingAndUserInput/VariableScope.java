package section6.ExceptionHandlingAndUserInput;

public class VariableScope {

    //  VARIABLE SCOPE is the accessibility of a variable;
    //  LOCAL VARIABLE is IN SCOPE (accessible) inside the code block where it was declared, or nested code blocks; 
    //  Outside that code block it is OUT OF SCOPE;
    //  
    //  DECLARATION of a variable means declaring its name and data type;
    //  int myInt;
    //  INITIALIZATION of a variable means atributing a value to a previously declared variable;
    //  myInt = 1;
    //  
    //  NESTED BLOCK is a block inside a block;
    //  
    //  GOOD PRACTICES
    //  To declare and initialize variables on the same block if possible;
    //  To declare variables on the narrowest possible scope;

}
