package section16;

public class FinalModifier {
    
    //  The FINAL Modifier

    //  A final FIELD cannot be reassigned or be given a diferent value (i.e. change it's STATE) after it's OWN INITIALIZATION;
    //  A final STATIC FIELD cannot change it's state after the CLASS INITIALIZATION;
    //  A field declared on an INTERFACE is always Public, Static and Final;
    //  A final METHOD cannot be OVERRIDDEN by a subclass;
    //  A final STATIC METHOD means subclasses can't HIDE it;
    //  A final CLASS cannot be overriden, i.e. EXTENDED by subclasses;
    //  A final variable in a code block cannot be modified after being assigned a value;
    //  A final method parameter cannot be assigned a different value in the methdod code block;

}
