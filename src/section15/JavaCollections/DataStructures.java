    //  The COLLECTIONS FRAMEWORK is divided into 3 interfaces:

    //  LIST interface:
    //  A List is an ORDERED collection (a.k.a. SEQUENCE);
    //  They can be sequenced in memory (ARRAYLIST);
    //  Or linked to next and previous elements (LINKEDLIST); 

    //  QUEUE interface:
    //  A Queue is designed to hold elements prior to processing;
    //  The order, as well as the first (HEAD) and last (TAIL) position matter;
    //  FirstInFirstOut FIFO;
    //  LastInFirstOut LIFO (STACK);
    //  DEQUE implementation supports both FIFO and LIFO;

    //  SET interface:
    //  A Set is a collection based on the mathematical concept of a Set:
    //          no duplicates;
    //          no natural order/sequencing;
    //  Implementations (HASHSET; LINKEDHASHSET; TREESET) differ in the way they store elements in the set;
    //  SORTEDSET has ordered elements;

    //  MAP (does not implement interface, but is considered part of framework):
    //  A Map is a collection that stores KEY and VALUE pairs;
    //  Keys are a SET, Values are a separate collection;
    //  Each Key keeps a reference to a Value, so they must be UNIQUE!
    //  Elements in a TREE are stored in a Key Value Node, known as an ENTRY;


