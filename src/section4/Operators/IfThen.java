package section4.Operators;

public class IfThen {
    public static void main(String[] args) {
        
        boolean isGay = true;
        System.out.println("Why are you gay?");
        System.out.println("Who says I'm gay?");
        if (isGay == true) System.out.println("You are gay!");

        boolean isNotGay = true;
        if (isNotGay == false); System.out.println("You are gay!");

        if (isNotGay == false) {
            System.out.println("If you don't want Java calling you gay a second time,");
            System.out.println("Use curly braces and code blocks!");
        }
        
    }
}
